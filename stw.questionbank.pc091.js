// ----------------- Helpers (no UI assumptions) -----------------
const clamp = (v, a, b) => Math.max(a, Math.min(b, v));

export function getCategories() {
  return Object.entries(STW_QB).map(([key, v]) => ({ key, label: v.label || key }));
}

export function getSubcategories(categoryKey) {
  const cat = STW_QB?.[categoryKey];
  const subs = cat?.subs || {};
  return Object.entries(subs).map(([key, v]) => ({ key, label: v.label || key }));
}

// For dropdowns later: "Stage 4 (Years 7–8) / Maths — Number"
export function getSubcategoryOptions() {
  const out = [];
  for (const [ck, cat] of Object.entries(STW_QB)) {
    for (const [sk, sub] of Object.entries(cat.subs || {})) {
      out.push({ category: ck, sub: sk, label: `${cat.label || ck} / ${sub.label || sk}` });
    }
  }
  return out;
}

// Random question with safe fallbacks.
// If subcategoryKey omitted, pulls across ALL subs inside the category.
export function getRandomQuestion(categoryKey, difficulty = 1, subcategoryKey = null) {
  const categories = Object.keys(STW_QB);
  const catKey = STW_QB?.[categoryKey] ? categoryKey : (categories[0] || null);
  const d = clamp(Number(difficulty) || 1, 1, 3);

  if (!catKey) {
    return { category: null, subcategory: null, difficulty: d, question: 'No categories in bank.', answer: '—' };
  }

  const cat = STW_QB[catKey];
  const subs = cat?.subs || {};

  let pool = [];
  if (subcategoryKey && subs?.[subcategoryKey]) {
    pool = subs[subcategoryKey]?.d?.[d] || [];
  } else {
    for (const sub of Object.values(subs)) {
      const arr = sub?.d?.[d];
      if (Array.isArray(arr) && arr.length) pool.push(...arr);
    }
  }

  if (!pool.length) {
    return { category: catKey, subcategory: subcategoryKey || null, difficulty: d, question: 'No questions in this bank.', answer: '—' };
  }

  const item = pool[Math.floor(Math.random() * pool.length)];
  return {
    category: catKey,
    subcategory: subcategoryKey || null,
    difficulty: d,
    question: String(item?.q || ''),
    answer: String(item?.a || '')
  };
}
